def evaluate_board(board):
    """
    Evaluates the board state.
    Positive result means White is winning, negative means Black.
    """
    if board.is_checkmate():
        if board.turn == chess.WHITE:
            return -99999
        else:
            return 99999
    if board.is_stalemate() or board.is_insufficient_material():
        return 0

    piece_values = {
        chess.PAWN: 100,
        chess.KNIGHT: 320,
        chess.BISHOP: 330,
        chess.ROOK: 500,
        chess.QUEEN: 900,
        chess.KING: 20000
    }
    
    score = 0
    for square in chess.SQUARES:
        piece = board.piece_at(square)
        if piece:
            # Material value
            val = piece_values.get(piece.piece_type, 0)
            
            # Positional value from PST
            # For black, we flip the board vertically to use the same table
            pst_val = 0
            if piece.piece_type in PST:
                pst_idx = square if piece.color == chess.WHITE else square ^ 56
                pst_val = PST[piece.piece_type][pst_idx]
            
            if piece.color == chess.WHITE:
                score += (val + pst_val)
            else:
                score -= (val + pst_val)
    
    return score

def alpha_beta(board, depth, alpha, beta, is_maximizing):
    """
    Alpha-Beta Pruning implementation.
    'depth' is how many moves ahead we look.
    """
    if depth == 0 or board.is_game_over():
        return evaluate_board(board)

    if is_maximizing:
        max_eval = -float('inf')
        for move in board.legal_moves:
            board.push(move)
            eval = alpha_beta(board, depth - 1, alpha, beta, False)
            board.pop()
            max_eval = max(max_eval, eval)
            alpha = max(alpha, eval)
            if beta <= alpha:
                break
        return max_eval
    else:
        min_eval = float('inf')
        for move in board.legal_moves:
            board.push(move)
            eval = alpha_beta(board, depth - 1, alpha, beta, True)
            board.pop()
            min_eval = min(min_eval, eval)
            beta = min(beta, eval)
            if beta <= alpha:
                break
        return min_eval

def get_best_move(board, depth=3):
    """
    Finds the best move using search.
    """
    best_move = None
    best_value = -float('inf') if board.turn == chess.WHITE else float('inf')
    
    for move in board.legal_moves:
        board.push(move)
        board_value = alpha_beta(board, depth - 1, -float('inf'), float('inf'), not board.turn)
        board.pop()
        
        if board.turn == chess.WHITE:
            if board_value > best_value:
                best_value = board_value
                best_move = move
        else:
            if board_value < best_value:
                best_value = board_value
                best_move = move
                
    return best_move
